%% Function: 
%% Description:
%%--------------------------------------------------------------------
start() ->
    application:load(myapp),
    start_webserver(inets).

start_webserver(inets) ->
    inets:stop(),
    application:set_env(inets, services, [{httpd, filename:join(["/home/alex/projects/erlang/my-framework", "config", "inets.conf"])}]),
    
    inets:start().

